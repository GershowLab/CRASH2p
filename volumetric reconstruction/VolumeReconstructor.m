        function vr = addPhaseCountCorrection(vr, beamnum, phase, corr_factor)
            % vr = addPhaseCountCorrection(vr, beamnum, phase, corr_factor)
            %
            % apply a correction to each photon based on the phase of the
            % tag lens
            % beamnum - 1,2
            % phase, corr_factor lookup table for correction to be applied
            pe = interp1(phase, corr_factor, [0 2*pi], 'linear', 'extrap');
            if (min(phase) > 0)
                phase = [0 phase];
                corr_factor = [pe(1) corr_factor];
            end
            if (max(phase) < 2*pi)
                phase = [phase 2*pi];
                corr_factor = [corr_factor pe(end)];
            end
            vr.phaseCountCorrector{beamnum} = griddedInterpolant(phase, corr_factor, 'linear');
            vr.phaseCorrectCounts(beamnum) = true;
        end
        
        function countcorrector = getPhaseCountCorrection(vr, beamnum, color, timerange)
            % calculates the phase count corrector based on histogram
            % normalization
            % designed to be used with reference volume, but maybe instead,
            % we should scan the actual target while moving up and down in
            % z a lot to blur the structure
            
            [~, pphase] = vr.getPhotonLocations(beamnum, color, timerange);
            [~,beam] = vr.vrfa.getData(beamnum, mean(timerange)+[-5 5]);
            nbins = round(mean(beam.tagPeriod)*vr.FPGA_CLOCK);
            pe = linspace(0, 2*pi, nbins+1);
            pcounts = histcounts(mod(pphase,2*pi), pe);
            ps = lowpass1D([pcounts pcounts pcounts], 5); ps = ps(nbins + (1:nbins));
            pcorr = median(ps)./ps;
            countcorrector.phase = 0.5*(pe(1:(end-1))+(pe(2:end)));
            countcorrector.corr_factor = pcorr;
            countcorrector.count = pcounts;
            countcorrector.count_smooth = ps;
        end
        
