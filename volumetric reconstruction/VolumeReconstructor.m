%                 [im_g,~,ct_g,dw_g] = vr.getXYZProjs(xe,ye,ze,ranges,scanbeam,'g',0);
%                 dw_g = flip(dw_g.xy',2); % this way orientation looks right in ImageJ (different from playMovie_customizable)
%                 [im_r,~,ct_r,dw_r] = vr.getXYZProjs(xe,ye,ze,ranges,scanbeam,'r',0);
%                 dw_r = flip(dw_r.xy',2);
                [ct_g,dw_g,~] = vr.binXYZ(scanbeam,'g',xe,ye,ze,ranges.t);
                ct_g(dw_g==0) = NaN;
                [ct_r,dw_r,~] = vr.binXYZ(scanbeam,'r',xe,ye,ze,ranges.t);
                ct_r(dw_r==0) = NaN;
                switch mode
                    case 'count'
%                         ct_g = flip(ct_g.xy',2);
%                         ct_g(dw_g==0) = NaN;
%                         v_g(:,:,i) = ct_g;
%                         ct_r = flip(ct_r.xy',2);
%                         ct_r(dw_r==0) = NaN;
%                         v_r(:,:,i) = ct_r;
                        v_g(:,:,:,i) = ct_g;
                        v_r(:,:,:,i) = ct_r;
                    case 'rate'
%                         im_g = flip(im_g.xy',2);
%                         im_g(dw_g==0) = NaN;
%                         v_g(:,:,i) = im_g;
%                         im_r = flip(im_r.xy',2);
%                         im_r(dw_r==0) = NaN;
%                         v_r(:,:,i) = im_r;
                        im_g = ct_g./dw_g;
                        v_g(:,:,:,i) = im_g;
