            v_smoo.red = vs;
            err.red = er;
%             vp.xy = squeeze(flip(permute(sum(vs,3),[2 1 3 4]),2));
%             vp.xz = squeeze(flip(permute(sum(vs,2),[1 2 3 4]),2));
%             vp.zy = squeeze(flip(permute(sum(vs,1),[1 3 2 4]),2));
%             v_smoo.redproj = vp;
            v.green = v_g;
            v.red = v_r;
            
