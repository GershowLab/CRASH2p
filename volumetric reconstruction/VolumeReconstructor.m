        function [pos, phase, tvec, dwelltime, offset] = getDwellLocationSubsampled(vr, beamnum, color, trange, deltaT, galvoDeltaT, offsetCorrect, scanCorrect)
            % [pos, phase, tvec, dwelltime, offset] = getDwellLocationSubsampled(vr, beamnum, color, trange, deltaT, galvoDeltaT)
             %
             % gets interpolated location of beam at time intervals specified by deltaT
             % 
             % 
             % beamnum = 1,2 which beam
             % deltaT - how often to sample dwell location
             % trange = time interval in which dwell location is returned returned
             % color (optional) - if 'g', beam misalignment applied
             % galvoDeltaT (optional) - override vr setting
             % pos = x,y location of galvo (+ aod) at each dwell point
             % phase = phase at each dwell point
             % tvec = time when dwell location is recorded
             % dwelltime = basically just deltaT - dwell time at each
             % point
             
            existsAndDefault('galvoDeltaT', vr.galvoDeltaT);
            existsAndDefault('offsetCorrect', false);
            existsAndDefault('scanCorrect', false);
            if (~existsAndDefault('galvoDeltaT', 0))
                warning ('galvoDeltaT not set - using 0 deltaT');
            end
            [vr.vrfa.galvoDeltaT] = deal(galvoDeltaT);
          
            [pos, phase, tvec, dwelltime] = vr.vrfa.getDwellLocationSubsampled(beamnum, trange, deltaT);
            if existsAndDefault('color', '')
                if (color == 'g')
                    pos(1,:) = pos(1,:) - vr.colorMisalignment(1,beamnum);
                    pos(2,:) = pos(2,:) - vr.colorMisalignment(2,beamnum);
                end
            end
            if (nargout>4 || offsetCorrect)
                [xo,yo,zo] = vr.getOffset(tvec);
                offset = [xo;yo;zo];
            end
            if (offsetCorrect)
                pos(1:2,:) = pos(1:2,:) - offset(1:2,:);
            end
            if (scanCorrect)
                pos = vr.scanCorrectedPoints(pos,tvec);
            end
            phase = mod(phase-vr.getPhaseCorrection(beamnum, tvec), 2*pi);
          
        end
       
        function phi = getPhaseCorrection(vr, beamnum, time)
            % deltaphi = getPhaseCorrection(vr, beamnum, time)
            % gets phase adjustment for tag lens of beamnum at time(s)
            maxlen = 1e6;
            if (length(time) > maxlen)
                delta = ceil(length(time)/maxlen);
                phi1 = vr.getPhaseCorrection(beamnum, time(1:delta:end));
                phi = reshape(repmat(phi1, [delta 1]), 1, []);
                phi = phi(1:length(time));
                return;
            end
            if (isempty(vr.phaseCorrection) || ~isstruct(vr.phaseCorrection))
                phi = zeros(size(time));
            else
                phi = polarinterp1(vr.phaseCorrection(beamnum).t, vr.phaseCorrection(beamnum).phi, time, 'linear');
            end
        end
        
        function dwellcorrection = getDwellCorrection (vr, tvec)
            if (vr.rateCorrection && (isa(vr.trackerRateTarget, 'function_handle') || (vr.trackerRateTarget > 0)) && isa(vr.tsr, 'TrackingScopeResult'))
                if isa(vr.trackerRateTarget, 'function_handle')
                    dwellcorrection = (interp1(vr.tsr.tx, vr.tsr.neuron.redrate, tvec)/vr.trackerRateTarget(tvec));
                else
                    dwellcorrection = (interp1(vr.tsr.tx, vr.tsr.neuron.redrate, tvec)/vr.trackerRateTarget);
                end
            else
                dwellcorrection = ones(size(tvec));
            end
        end
        
