        function vr = calculateScanBox(vr,beamnum,color)
            
            existsAndDefault('beamnum',2);
            existsAndDefault('color','r');
            
            nframe = numel(vr.frame.edges)-1;
            corners = NaN(4,nframe);
            center = NaN(2,nframe);
            for i=1:nframe
                trange = vr.frame.edges(i+[0 1]);
                %dwell location subsampled is inappropriate here, because
                %we are not SUB sampling (sampling rate faster than tag frequency)
               % pos = vr.getDwellLocationSubsampled(beamnum,color,trange,dt,vr.galvoDeltaT,true,false);
               pos = vr.getDwellLocations(beamnum, color, trange, [], true); 
                cnr = prctile(pos(1:2,:)',[1 99 50]);
                xmin = cnr(1,1); xmax = cnr(2,1);
                ymin = cnr(1,2); ymax = cnr(2,2);
                corners(:,i) = [xmin;xmax;ymin;ymax];
                center(:,i) = cnr(3,:)';
            end
            
            % fill missing values using previous non-missing ones
            corners = fillmissing(corners','previous')';
            center = fillmissing(center','previous')';
%            center = [mean(corners(1:2,:),1,'omitnan');mean(corners(3:4,:),1,'omitnan')];
            
%             globalbox = max(abs(corners),[],'all')*[-1 1 -1 1];
            %could change this to percentile to make more robust against
            %outliers
            globalbox = [min(corners(1,:)) max(corners(2,:)) min(corners(3,:)) max(corners(4,:))]; %xmin xmax ymin max
            
            vr.scanBox.corners = corners;
            vr.scanBox.center = center;
            vr.scanBox.globalBox = globalbox;
        end

        
        function vr = calculateScanWindow(vr, beamnum, color, dt)
            existsAndDefault('beamnum',2);
            existsAndDefault('color','r');
            existsAndDefault('dt',1/vr.frame.framerate_Hz); % sampling rate
            t = min(vr.vrfa.getTimeRange):dt:max(vr.vrfa.getTimeRange);
            n = length(t)-1;
            sw = repmat(struct('xrange', [],'yrange',[], 'center',[0 0]), [1 n], 'bbox', []);
            for i=1:n
                try
                    dpos = vr.getDwellLocations(beamnum, color, t([i i+1]),[],true);
                    xp = percentile(dpos(1,:), [0.01 0.5 .99]); %find range and center - exclude stray edges
                    yp = percentile(dpos(2,:), [0.01 0.5 .99]); %find range and center - exclude stray edges
                    sw(i).xrange = xp([1 3]);
                    sw(i).yrange = yp([1 3]);
                    sw(i).center = [xp(2) yp(2)];
                    [rx,ry] = minboundrect(dpos(1,:), dpos(2,:));
                    sw(i).bbox = [rx(1:4) ry(1:4)];
                catch
                end
            end
            vr.scan
            
        end
        
        function vr = calculateScanOffset(vr,beamnum,color,dt,tw)
            % DEPRECATED
            existsAndDefault('beamnum',2);
            existsAndDefault('color','r');
            existsAndDefault('dt',1/vr.frame.framerate_Hz); % sampling rate
            existsAndDefault('tw',1E-2); % time window for moving min/max filter within each sampling period
            
            t = min(vr.vrfa.getTimeRange):dt:max(vr.vrfa.getTimeRange);
            t = t+dt/2;
            t = t(1:end-1);
            n = numel(t);
            scanoff = NaN(3,n);
            figure;
            for i=1:n
                trange = [t(i)-dt/2 t(i)+dt/2];
                try
                    [pos,~,T,~] = vr.getDwellLocations(beamnum,color,trange,vr.galvoDeltaT,true);
                    m = ceil((tw/mean(T))/2)*2;
                    maxs = movmax(pos,m,2,'Endpoints','discard');
                    mins = movmin(pos,m,2,'Endpoints','discard');
                    c = mean((maxs+mins)/2,2);
                catch
                    c = NaN(3,1);
                end
                scanoff(:,i) = c;
                if mod(round(t(i)),15)==0 || i==n
                    plot(t(1:i),scanoff(1:2,1:i)); legend('offset x','offset y');
                    title('calculating scan offset'); xlabel('t (s)'); ylabel('offset (\mum)');
                    drawnow;
                end
            end
            
            % resample scan offset and add to current offset
            if ~isempty(vr.offset)
                trackt = vr.offset.tx;
                trackoff = vr.offset.loc;
                scanoff = interp1(t,scanoff',trackt,'nearest')';
                scanoff(~isfinite(scanoff)) = 0;
                newoff = trackoff+scanoff;
                % apply changes to vr.offset
                vr.offset.loc(1:2,:) = newoff(1:2,:);
                vr.offset.x = griddedInterpolant(vr.offset.tx,newoff(1,:),'linear','nearest');
                vr.offset.y = griddedInterpolant(vr.offset.tx,newoff(2,:),'linear','nearest');
            else
                % assume tracking=false
                vr.offset.tx = t;
                vr.offset.tracking = false(size(t)); % match data structure for when tracking=true
                vr.offset.tagAmpl =  vr.settings(end).analogScalingFactors(beamnum).zTagMicronsPerTick/(2*pi*vr.settings(end).analogScalingFactors(beamnum).tagLensFrequency)*8e7; % same as above
                vr.offset.loc = scanoff;
                vr.offset.x = griddedInterpolant(vr.offset.tx,scanoff(1,:),'linear','nearest');
                vr.offset.y = griddedInterpolant(vr.offset.tx,scanoff(2,:),'linear','nearest');
                vr.offset.z = griddedInterpolant(vr.offset.tx,scanoff(3,:),'linear','nearest');                
            end
        end

        function vr = applyTrackerOffset(vr, beamnum) 
            %x,y offset are location of the tracked neuron in x,y
            %z offset is (location of the tracked neuron - piezo)/tagAmpl -- = cos(phase) of the offset 
            existsAndDefault('beamnum', 1);
            vr.offset.tx = vr.tsr.tx;
            vr.offset.tracking = vr.tsr.tracker.tracking;
            vr.offset.tagAmpl =  vr.settings(end).analogScalingFactors(beamnum).zTagMicronsPerTick/(2*pi*vr.settings(end).analogScalingFactors(beamnum).tagLensFrequency)*8e7;
            
            ts = vr.settings(end).TrackerSettings;
            if (ts.velocityTracking.x || ts.velocityTracking.y || ts.velocityTracking.z)
                loc = vr.tsr.tracker.loc; %we don't have the data (I think) for smoothing if velocity tracking is on
            else
                g = [ts.comGain.x_y ts.comGain.x_y ts.comGain.z]*ts.circleRadius_um_;
                vr.tsr = vr.tsr.applySmoother(g);
                loc = vr.tsr.tracker.loc_smooth;
            end
            loc(3,:) = (loc(3,:) - vr.tsr.tracker.piezo)/vr.offset.tagAmpl; %offset is in terms of fraction of the tag amplitude
            loc = lowpass1D(loc, 3);
            vr.offset.loc = loc;
            
            vr.offset.x = griddedInterpolant(vr.offset.tx, loc(1,:), 'linear', 'nearest');
            vr.offset.y = griddedInterpolant(vr.offset.tx, loc(2,:), 'linear', 'nearest');
            vr.offset.z = griddedInterpolant(vr.offset.tx, loc(3,:), 'linear', 'nearest');
            
            vr.useoffset = true;
            return;
            
            f0 = vr.settings(end).TrackerSettings.circleFrequency_Hz_;
            rs = vr.settings(end).TrackerSettings.circleRadius_um_;
            si = find(diff([0 vr.offset.tracking]) > 0);
            ei = find(diff([vr.offset.tracking 0]) < 0);
            
            for j = 1:length(si)
%                 xx = x(si(j):ei(j))-lowpass1D(x(si(j):ei(j)), 200);
%                  w = blackmanharris(length(xx), 'periodic');
%                 [Xm,f] = periodogram(xx-mean(xx), w, length(xx), 1/median(diff(vr.offset.time(si(j):ei(j))))); [~,I] = max(Xm .* (f > 100 & f < 1e4)); f0 = f(I);
%                
                dt = round(3e-3*f0)/f0;
              %  rs = mean(rscan(si(j):ei(j)), 'omitnan');
             %   fprintf(1, 'detected tracker frequency: %.1f and radius: %.2f\n', f0, rs);
                trange = [vr.offset.ttsr(si(j)) + dt,  vr.offset.ttsr(ei(j))];
                tx = trange(1):dt:trange(2);
                [ploc, pp, tp] = vr.getPhotonLocations(beamnum, color, trange);
        
                if (any(~isfinite(ploc(:))) || any(~isfinite(pp)))
                    warning('something foobar');
                end
                
                s = 0.2;
%                 fprintf ('cos(phase) stddev = %.4f\n',s); 
                rmeas = 0.5*([rs.^2 rs.^2 s.^2]);
                 vr.offset.offsetfit(j).tx =tx;
                [vr.offset.offsetfit(j).theta,vr.offset.offsetfit(j).w,vr.offset.offsetfit(j).thetas,vr.offset.offsetfit(j).ws] = ...
                    locationEstimateKalman(tp, [ploc(1:2,:);cos(pp)], D, tx, rmeas, ones([1 3]));
                
%                 inds = vr.offset.time >= tx(1) & vr.offset.time <= tx(end);
%                 vr.offset.loc(:,inds) = interp1(tx, vr.offset.offsetfit(j).thetas(1:3,:)', vr.offset.time(inds), '*linear', 0)';
            end
            vr.offset.tx = cat(2, vr.offset.offsetfit.tx);
            locvel = cat(2, vr.offset.offsetfit.thetas);
            vr.offset.loc = locvel(1:3,:);
            vr.offset.x = griddedInterpolant(vr.offset.tx, locvel(1,:), 'linear', 'nearest');
            vr.offset.y = griddedInterpolant(vr.offset.tx, locvel(2,:), 'linear', 'nearest');
            vr.offset.z = griddedInterpolant(vr.offset.tx, locvel(3,:), 'linear', 'nearest');
            
            
        end
        
