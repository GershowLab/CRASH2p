                        v_r(:,:,:,i) = im_r;
                    otherwise
                        warning('mode can only be ''count'' or ''rate''!');
                        return;
                end
            end
            
            % do smoothing
            disp('smoothing green fluorescence data...');
            [vs,er] = l1spline(v_g,smoothopts.s,smoothopts.l,smoothopts.nBreg,smoothopts.nInner,smoothopts.eps);
