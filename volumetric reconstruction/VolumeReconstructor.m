        function vr = loadBehaviorVideo(vr)
            try
                v = VideoReader(vr.behavior.videoFileName);
                % matlab is annoying: VideoReader.read() can read all
                % frames but will be removed in the future; we only have
                % VideoReader.readFrame()
                % assume we can always trust the frame_number column in
                % behav_TIMESTAMP.txt:
                video = zeros(v.Height,v.Width,length(vr.behavior.et));
                ind = 0;
                while hasFrame(v)
                    ind = ind+1;
                    fr = readFrame(v);
                    if (size(fr,3)>1)
                        fr = rgb2gray(fr);
                    end
                    video(:,:,ind) = fr;
                end
                vr.behaviorVideo = video;
                vr.hasBehaviorVideo = true;
            catch me
                disp(me.getReport());
                vr.hasBehaviorVideo = false;
            end
