        function tranges = getStillTimes(vr, trange, varargin)
            existsAndDefault('pctl', 0.1);
            existsAndDefault('trange', vr.offset.tx([1 end]));
            existsAndDefault('tagampl', 20);
            existsAndDefault('mintime', 0.5);
            varargin = assignApplicable(varargin);
            xx = vr.offset.loc; xx(3,:) = tagampl*xx(3,:);
            tx = vr.offset.tx;
            sp = sqrt(sum(deriv(xx, 0.25/median(diff(tx))).^2));
            slow = sp < percentile(sp,pctl);
            ts = tx(diff([false slow]) > 0);
            te = tx(diff([slow false]) < 0);
            valid = ts > min(trange) & te < max(trange) & te - ts > mintime;
            tranges = [ts(valid);te(valid)];
            
            figure(1); clf(1); plot(tx, sp, 'b-'); hold on;
            for j = 1:size(tranges,2)
                ii = tx > tranges(1,j) & tx < tranges(2,j);
                plot (tx(ii), sp(ii), 'r-');
            end
            hold off
            
        end
        
        function trange = getStillTimes2(vr,w,W,visualize,validtrange)
            existsAndDefault('w',0.1); % lowpass filter window (default=0.1s)
            existsAndDefault('W',1); % trange window (default=1s)
            existsAndDefault('visualize', false);
            existsAndDefault('validtrange',vr.tsr.tx([1 end]));
            % get low-passed larva motion data
            sig = w/vr.tsr.dt;
            t = vr.tsr.tx;
            x = lowpass1D(vr.tsr.tracker.loc(1,:),sig);
            y = lowpass1D(vr.tsr.tracker.loc(2,:),sig);
            vx = diff(x); vx = lowpass1D(vx,sig); vx = [vx(1) vx];
            vy = diff(y); vy = lowpass1D(vy,sig); vy = [vy(1) vy];
            s = sqrt(vx.^2+vy.^2); s = lowpass1D(s,sig);
            
            % remove invalid parts (i.e. not tracking or out of validtrange)
            valid = vr.tsr.tracker.tracking;
            valid(vr.tsr.tx<min(validtrange)) = false;
            valid(vr.tsr.tx>max(validtrange)) = false;
            x(~valid) = NaN;
            y(~valid) = NaN;
            vx(~valid) = NaN;
            vy(~valid) = NaN;
            s(~valid) = NaN;
            validinds = find(valid);
            
            % get slowest segment by signal searching
            s_sim = zeros(1,round(W/vr.tsr.dt));
            [istart,istop,~] = findsignal(s(valid),s_sim);
            tstart = vr.tsr.tx(validinds(istart));
            tstop = vr.tsr.tx(validinds(istop));
            trange = [tstart tstop];
            
            % visualization
            if (~visualize)
                return;
            end
            figure;
            subplot(3,1,1);
            plot(t,x); hold on;
            plot(t,y);
            xticks([]); yl = ylim(gca);
            rectangle('Position',[tstart,yl(1),tstop-tstart,diff(yl)],'EdgeColor','r'); hold off;
            title('location'); legend('x','y');
            subplot(3,1,2);
            plot(t,vx); hold on;
            plot(t,vy);
            xticks([]); yl = [-1 1]; ylim(yl);
            rectangle('Position',[tstart,yl(1),tstop-tstart,diff(yl)],'EdgeColor','r'); hold off;
            title(['velocity (low pass filtered with ' sprintf('%0.1f',w) 's window)']); legend('x','y');
            subplot(3,1,3);
            plot(t,s); hold on;
            yl = [0 1]; ylim(yl); xlabel('t (s)');
            rectangle('Position',[tstart,yl(1),tstop-tstart,diff(yl)],'EdgeColor','r'); hold off;
            title(['speed (low pass filtered with ' sprintf('%0.1f',w) 's window)']);
            sgtitle('detecting when larva is immobile');
            drawnow;
            
            
        end
        
