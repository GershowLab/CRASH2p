        function vr = getStage(vr, stagename, eventname)
            
            existsAndDefault('stagename', strrep(vr.filename, '_photontiming.bin', '_stage_enc.txt'));
            existsAndDefault('eventname', strrep(vr.filename, '_photontiming.bin', '_event_log.txt'));
            if (~exist(stagename, 'file'))
                d = dir(fileparts(vr.filename), '*_stage.txt');
                if (~isempty(d))
                    stagename = fullfile(d.folder, d.name);
                else
                    error ('can''t find stage file');
                end
            end
            if (~exist(eventname, 'file'))
                d = dir(fileparts(vr.filename), '*_event_log.txt');
                if (~isempty(d))
                    eventname = fullfile(d.folder, d.name);
                else
                    error ('can''t find event log file');
                end
            end
            
            
            enc = parseStageEncoder(stagename, eventname);
            vr.stage.enc = enc;
            vr.stage.eti = enc.fpgatimer(enc.valid)'/vr.FPGA_CLOCK;
            vr.stage.x = enc.loc(1, enc.valid)';
            vr.stage.y =  enc.loc(2, enc.valid)';
            vr.stage.z =  enc.loc(3, enc.valid)';
            
            vr.stage.sp = sqrt(deriv(vr.stage.x, 4).^2 + deriv(vr.stage.y, 4).^2 + deriv(vr.stage.z, 4).^2)./deriv(vr.stage.eti, 1);
            vr.stage.atrest = vr.stage.sp < 1; %1 micron/sec
        end
        
        function vr = getStageEpochs(vr)
            si = find(diff([false; vr.stage.atrest]) > 0);
            ei = find(diff([vr.stage.atrest; false]) < 0);
            vr.stage.epoch = [];
            for j = 1:length(si)
                vr.stage.epoch(j).trange = vr.stage.eti([si(j) ei(j)]);                
                vr.stage.epoch(j).x = mean(vr.stage.x(si(j):ei(j)));
                vr.stage.epoch(j).y = mean(vr.stage.y(si(j):ei(j)));
                vr.stage.epoch(j).z = mean(vr.stage.z(si(j):ei(j)));
                vr.stage.epoch(j).loc = [vr.stage.epoch(j).x;vr.stage.epoch(j).y;vr.stage.epoch(j).z];
                for k = 1:2
                    vr.stage.epoch(j).beam(k).rNum = length (vr.getPhotonLocations(k, 'r', vr.stage.epoch(j).trange));
                    vr.stage.epoch(j).beam(k).gNum = length (vr.getPhotonLocations(k, 'g', vr.stage.epoch(j).trange));                  
                end
            end
        end
        
        
