        function saveSmoothedDataAndTiff(vr, v_smoo, v, err, varargin)
            % v_smoo,v,err: outputs of vr.getSmoothedData()
            % default: save TIMESTAMP_smoothing.mat/tif to data set folder
            dstdir = []; % default: data set folder
            fname = []; % default: TIMESTAMP_smoothing.mat/tif
            fpath = []; % default: build from default dstdir and fname
            saveopts = struct('overwrite',true,'message',false); % see saveastiff for available options
            
            assignApplicable(varargin);
            
            % get default dstdir
            srcdir = fileparts(vr.filename);
            if isempty(dstdir)
                dstdir = srcdir;
            end
            % get default fname
            timestamp = split(srcdir,filesep);
            timestamp = timestamp{end};
            if isempty(fname)
                fname = [timestamp '_smoothing.mat'];
            end
            % build default fpath from default dstdir and fname
            if isempty(fpath)
                fpath = fullfile(dstdir,fname);
            end
            % create folder if not already exists, fix extension to .mat
            [dstdir,fname,~] = fileparts(fpath);
            if ~exist(dstdir,'dir')
                mkdir(dstdir);
            end
            fpath = fullfile(dstdir,[fname '.mat']);
            
            % save all data to one .mat file
            disp(['saving smoothed data to ' fpath]);
            save(fpath,'v_smoo','v','err');
            % save both green and red data to one .tiff file in order xyztc
            s = size(v_smoo.green);
            gstack = reshape(v_smoo.green,s(1),s(2),[]);
            gstack = flip(permute(gstack,[2 1 3]),2);
            rstack = reshape(v_smoo.red,s(1),s(2),[]);
            rstack = flip(permute(rstack,[2 1 3]),2);
            rgstack = cat(3,rstack,gstack);
            fpath = strrep(fpath,'.mat','_xyztc.tif');
            disp(['saving smoothed image stack to ' fpath]);
            saveastiff(rgstack,fpath,saveopts);
            
            disp('...done!');
        end
                
