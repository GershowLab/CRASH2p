            if ~isfield(ranges,'z')
                ranges.z = [-1 1];
            end
            if isempty(voxelsize)
                voxelsize = [is.pixelSize_Microns is.pixelSize_Microns 2/is.numZPixels];
            end
            xe = min(ranges.x):voxelsize(1):max(ranges.x);
            ye = min(ranges.y):voxelsize(2):max(ranges.y);
            ze = min(ranges.z):voxelsize(3):max(ranges.z);
            
            % parse l1spline smoothing params
            if ~isfield(smoothopts,'s')
                smoothopts(1).s = 5E-2; % main smoothing param
            end
            if ~isfield(smoothopts,'l')
                smoothopts(1).l = 1; % split bregman smoothing param (lambda in paper)
            end
            if ~isfield(smoothopts,'nBreg')
                smoothopts(1).nBreg = 500; % number of split bregman iterations
            end
            if ~isfield(smoothopts,'nInner')
                smoothopts(1).nInner = 1; % number of inner iterations
            end
            if ~isfield(smoothopts,'eps')
                smoothopts(1).eps = 1E-3; % acceptable relative error
            end
            
            % get original data with missing values
            disp('getting original data with missing values...');
            tedges = min(trange):tstep:max(trange);
            ranges.t = tedges(1:2);
            [~,ax] = vr.getAssemblerAndAxes(xe,ye,ze);
