        function vr = VolumeReconstructor(filename, maxBytes, parseFile)
            %function vr = VolumeReconstructor(filename, maxBytes, parseFile)
            %   vr < VolumeReconstructor
            %   filename < str : path to photontiming.bin file, or to directory
            %       containging photontiming.bin file
            %   praseFile < bool : whether to parse the data file to get
            %   number of photons/tags per rollover for more accurate error
            %   checking; defaults to false
            if (nargin > 0)
                vr.filename = filename;
                if (exist(vr.filename, 'dir'))
                    d = dir(fullfile(vr.filename, '*_photontiming.bin'));
                    if (~isempty(d))
                        vr.filename = fullfile(d.folder, d.name);
                    else
                        warning ('couldn''t find file');
                        vr.filename = '';
                    end
                end
            end
            if (nargin > 1)
                vr.maxBytes = maxBytes;
            else
                if ispc
                    m = memory;
                    vr.maxBytes = m.MemAvailableAllArrays/2;
                elseif isunix
                    [~,w] = unix('free | grep Mem');
                    stats = str2double(regexp(w,'[0-9]*','match')); % unit in KB
                    memAvailable = (stats(3)+stats(end))*1024; % unit in B
                    vr.maxBytes = memAvailable/2;
                end
            end
            if (~isempty(vr.filename))
                vr = vr.loadVolume();
                if (nargin > 2 && parseFile)
                    vr.vrfa = vr.vrfa.parseChunks();                    
                end
                [hasErrors, msg] = vr.vrfa.consistencyCheck();
                if (hasErrors)
                    fprintf('errors detected:\n');
                    fprintf('%s\n', msg{:});
                    fid = fopen(strrep(vr.filename, '_photontiming.bin', '_parse_error_message.txt'), 'at');
                    fprintf(fid, 'errors detected:\n');
                    fprintf(fid, '%s\n', msg{:});
                    fclose(fid);
                end
                try
                    fname = strrep(vr.filename, '_photontiming.bin', '_imaging_settings.json');
                    vr.settings = jsondecode(fileread(fname));
                    for j = 1:length(vr.settings)
                        
                        if (~isfield(vr.settings(j).imagingSettings, 'numXPixels'))
                            vr.settings(j).imagingSettings.numXPixels = ceil(vr.settings(j).imagingSettings.xScanRange_Microns/vr.settings(j).imagingSettings.pixelSize_Microns);
                        end
                        
                        if (~isfield(vr.settings(j).imagingSettings, 'numYPixels'))
                            vr.settings(j).imagingSettings.yScanRange_Microns = vr.settings(j).imagingSettings.xScanRange_Microns / vr.settings(j).imagingSettings.aspectRatio_x_y_;
                            vr.settings(j).imagingSettings.numYPixels = ceil(vr.settings(j).imagingSettings.yScanRange_Microns/vr.settings(j).imagingSettings.pixelSize_Microns);
                        end
                    end
                catch me
                    warning('ERROR during populating vr.settings!');
                    disp(me.getReport);
                end
                try
                    vr = vr.loadTracker();
                    vr = vr.applyTrackerOffset();
                catch me
                    disp(me.getReport);
                end
                try
                    vr = vr.loadEventData();
                catch me
                    disp(me.getReport);
                end
            end
        end
        
        function vr = changeFilename(vr, fname)
            % update _photontiming.bin file name
            if (isfile(fname))
                newfname = fname;
            else
                [~,f,e] = fileparts(vr.filename);
                if(isfile(fullfile(fname, [f e])))
                    newfname = fullfile(fname, [f e]);
                else
                    d = dir(fullfile(fname, '*_photontiming.bin'));
                    if (~isempty(d))
                        fprintf(1,'NOTICE: file source changed from %s to %s\n', [f e], d.name);
                        newfname = fullfile(d.folder, d.name);
                    else
                       fprintf(1,'NOTICE: could not change file directory/name - requested change: %s\n', fname);
                       return
                    end
                end
            end
            vr.filename = newfname;
            vr.vrfa = vr.vrfa.closeFile();
            vr.vrfa.filename = vr.filename;
            vr.vrfa = vr.vrfa.openFile();
            % update file names saved in vr.tsr
            newtsrfname = strrep(newfname,'_photontiming.bin','_tracker.txt');
            vr.tsr.tracker.filename = newtsrfname;
            newstagefname = strrep(newfname,'_photontiming.bin','_stage_enc.txt');
            vr.tsr.stage.filename = newstagefname;
            neweventlogfname = strrep(newfname,'_photontiming.bin','_event_log.txt');
            vr.tsr.event_log.filename = neweventlogfname;
            % update behavior video file name
            [d,~,~] = fileparts(vr.filename);
            [~,timestamp,~] = fileparts(d);
            newbehavfname = fullfile(d,'behavior_video',['behav_' timestamp '.avi']);
            if ~isfile(newbehavfname)
                d = dir(fullfile(d,'behavior_video','behav_*.avi'));
                if (~isempty(d))
                    [~,f,e] = fileparts(vr.behavior.videoFileName);
                    fprintf(1,'NOTICE: behavior video source changed from %s to %s\n', [f e], d.name);
                    newbehavfname = fullfile(d.folder, d.name);
                else
                    fprintf(1,'NOTICE: could not change behavior video source - requested change: %s\n', newbehavfname);
                    return
                end
            end
            vr.behavior.videoFileName = newbehavfname;
            if ~isempty(vr.tsr.behavior)
                vr.tsr.behavior.videoFileName = newbehavfname;
                if isfield(vr.tsr.behavior,'sleap')
                    oldsleapfname = vr.tsr.behavior.sleap.filename;
                    oldbehavdir = fileparts(oldsleapfname);
                    newbehavdir = fileparts(newbehavfname);
                    newsleapfname = strrep(oldsleapfname,oldbehavdir,newbehavdir);
                    vr.tsr.behavior.sleap.filename = newsleapfname;
                end
            end
        end
        
        function [phi, debug] =  calculatePhaseCorrectionChunk(vr, tx, sigma_s, beamnum, color)
             % [phi, debug] =  calculatePhaseCorrectionChunk(vr, tx, sigma_s, beamnum, color)
             
             if ~(vr.useoffset)
                 warning ('revised phase correction calculation procedure requires offset if tracking');
                 fprintf ('we take the bins with the most photons for phase correction\n');
                 fprintf ('so we need to correct out the center of mass motion, using tracker offset, before phase correction\n');
             end
             
             [pos, phase, time] = vr.vrfa.getPhotonLocations(beamnum, color, [min(tx)-3*sigma_s, max(tx) + 3*sigma_s]);
             [xo,yo] = vr.getOffset(time);
             if (isempty(phase))
                 phi = zeros(size(tx));
                 return;
             end
             
             pos = pos(1:2,:) - [xo;yo];
             
             xl = percentile(pos(1,:), [0.1 0.9]);
             yl = percentile(pos(2,:), [0.1 0.9]);
             nbins = 30;
             fa = FastAssembler(linspace(xl(1), xl(2), nbins), linspace(yl(1), yl(2), nbins));
             
             [bin,subs] = fa.binPhotons(pos(1,:), pos(2,:),phase); %use the sin(theta) correction, guessing that it's kind of close
             
              
             r  = FastAssembler.getBinRank(bin, subs);
            
             bin = bin(:);
             [~,I] = sort(bin, 'descend');
             totalphotons = cumsum(bin(I));
             cutoff = min(ceil(0.05*nbins^2), find(totalphotons  > 0.25*totalphotons(end), 1, 'first')); %top 5% of bins; bins comprising at least 25% of photons, whichever is greater
            
             
             phase = phase(r < cutoff);
             time = time(r < cutoff);
             
             

             
            lp = sigma_s > 0;
 
             weight = ones(size(phase));
             delta = median(diff(tx));
             txe = (min(tx)-3*sigma_s-delta/2):delta:(max(tx) + 3*sigma_s + delta/2);
             txc = (txe(1:end-1) + txe(2:end))/2;
             [~,~,bin] = histcounts(time, txe);
             valid = (bin > 0 & bin < length(txe));
             n =  accumarray(bin(valid)', weight(valid), size(txc'), @sum)';
             sp = accumarray(bin(valid)', weight(valid).*sin(phase(valid)), size(txc'), @sum)';
             cp = accumarray(bin(valid)', weight(valid).*cos(phase(valid)), size(txc'), @sum)';
             s2p = accumarray(bin(valid)', weight(valid).*sin(2*phase(valid)), size(txc'), @sum)';
             c2p = accumarray(bin(valid)', weight(valid).*cos(2*phase(valid)), size(txc'), @sum)';
             
             pt = 'mirror';
             if (lp)
                 n = lowpass1D(n, sigma_s, pt);
                 sp = lowpass1D(sp, sigma_s, pt);
                 cp = lowpass1D(cp, sigma_s, pt);
                 s2p = lowpass1D(s2p, sigma_s, pt);
                 c2p = lowpass1D(c2p, sigma_s, pt);
             end
             
             
             num = 2*sp.*cp./n.^2 - s2p./n;
             den = (cp.^2-sp.^2)./n.^2 - c2p./n;
             phi = 0.5*atan(num./den);
             lowden = abs(den) < 0.05;
             phi(lowden) = atan(sp(lowden)./cp(lowden));
           
              %now that we have a phase estimate, let's do a
             %correction that includes dwell time
             
             weight = abs(sin(phase-interp1(txc, phi,time)));  
             
             n =  accumarray(bin(valid)', weight(valid), size(txc'), @sum)';
             sp = accumarray(bin(valid)', weight(valid).*sin(phase(valid)), size(txc'), @sum)';
             cp = accumarray(bin(valid)', weight(valid).*cos(phase(valid)), size(txc'), @sum)';
             s2p = accumarray(bin(valid)', weight(valid).*sin(2*phase(valid)), size(txc'), @sum)';
             c2p = accumarray(bin(valid)', weight(valid).*cos(2*phase(valid)), size(txc'), @sum)';
             if (lp)
                 n = lowpass1D(n, sigma_s, pt);
                 sp = lowpass1D(sp, sigma_s, pt);
                 cp = lowpass1D(cp, sigma_s, pt);
                 s2p = lowpass1D(s2p, sigma_s, pt);
                 c2p = lowpass1D(c2p, sigma_s, pt);
             end
             
             
             num = 2*sp.*cp./n.^2 - s2p./n;
             den = (cp.^2-sp.^2)./n.^2 - c2p./n;
             phi = 0.5*atan(num./den);
             lowden = abs(den) < 0.05;
             phi(lowden) = atan(sp(lowden)./cp(lowden));
         
             
             phi = interp1(txc, phi, tx);
             debug.num = interp1(txc, num, tx);
             debug.den = interp1(txc, den, tx);
             debug.cp = interp1(txc, cp, tx);
             debug.sp = interp1(txc, sp, tx);
             debug.n = interp1(txc, n, tx);
             
            
             
        end
        
        function vr = calculatePhaseCorrection(vr)
        % vr = calculatePhaseCorrection(vr)
        % finds the phase shift that maximizes the square of the counts in
        % a z-histogram from cos(phi) = -0.6 to 0.6
            tr = vr.vrfa.getTimeRange;
            
            %each byte on disk seems to equal about 6 bytes in memory,
            %although this could change with composition; so load no more
            %than maxBytes/12 at a time?                        
            
            tx = tr(1):1:tr(end);
            
            tstops = vr.vrfa.getMemoryLimitedTimeSteps(tr, vr.maxBytes/24);           
            % out of memory for data set 20201110142128, reduced chunk size
            % to maxBytes/24 - Rui 20201112
            
            sigma = 3;
            
             if (isstruct(vr.phaseCorrection))
                vr.phaseCorrection = rmfield(vr.phaseCorrection, fieldnames(vr.phaseCorrection));
             end
            for b = 1:2
                vr.phaseCorrection(b).t = tx;
                vr.phaseCorrection(b).phi = zeros(size(tx));
            end
            i1 = 1;
            figure;
            for j = 2:(length(tstops))
                i2 = find(tx <= tstops(j), 1, 'last');
                 for b = 1:2
                     vr.phaseCorrection(b).phi(i1:i2) = vr.calculatePhaseCorrectionChunk(tx(i1:i2), sigma, b, 'a');
                 end
                 i1 = i2+1;
                 try
                     plot(tx(1:i2), [vr.phaseCorrection(1).phi(1:i2); vr.phaseCorrection(2).phi(1:i2)]);
                     title('calculating phase correction'); xlabel('t (s)'); ylabel('\phi');
                     drawnow; pause(0.001);
                 catch
                 end
            end
            return;
            
            tx = linspace(tr(1), tr(end), ceil(diff(tr)/3)); %about every 3 seconds 
            %op = optimset('Display', 'off');
           
            
            function nsh = negSqrCosHist(phase, delta)
                xe = linspace(-0.6,0.6,50);
                h = histcounts(cos(phase-delta), xe);
                nsh = -sum(h.^2);
                
            end
            figure;
            ts1 = tic;
            for j = 1:(length(tx)-1)
                for b = 1:2
                    vr.phaseCorrection(b).t(j) = tx(j);
                    try
                        [~, gphase] = vr.vrfa.getPhotonLocations(b,'g', tx(j + [0 1]));
                        [~, rphase] = vr.vrfa.getPhotonLocations(b,'r', tx(j + [0 1]));
                    catch
                        continue;
                    end
                    if (isempty(gphase) && isempty(rphase))
                        vr.phaseCorrection(b).phi(j) = 0;
                        continue;
                    end
                    if (isempty(gphase))
                        gw = 0;
                        gdelta = 0;
                    else
                        %objfun = @(delta) var(cos(gphase-delta));
                        objfun = @(delta) negSqrCosHist(gphase, delta);
                        gdelta = fminbnd(objfun, -pi/2, pi/2);
                        gw = length(gphase);
                    end
                    if (isempty(rphase))
                        rw = 0;
                        rdelta = 0;
                    else
                        %objfun = @(delta) var(cos(rphase-delta));
                        objfun = @(delta) negSqrCosHist(rphase, delta);
                        rdelta =  fminbnd(objfun, -pi/2, pi/2);
                        rw = length(rphase);
                    end
                   
                    vr.phaseCorrection(b).rdelta(j) = rdelta;
                    vr.phaseCorrection(b).gdelta(j) = gdelta;
                    vr.phaseCorrection(b).phi(j) = (rw*rdelta + gw*gdelta)/(rw+gw);
                   
                end 
                if (toc(ts1) > 10)
                    ts1 = tic;
                    try
                        plot(vr.phaseCorrection(1).t, cat(1,vr.phaseCorrection.phi));
                        title('calculating phase correction'); xlabel('t'); ylabel('phi');
                        drawnow; pause(0.001);
                    catch
                    end
                end
            end
            for b = 1:2
                vr.phaseCorrection(b).t(end+1) = tx(end);
                vr.phaseCorrection(b).phi(end+1) = vr.phaseCorrection(b).phi(end);
            end
        end
        
        function [vr,galvoDeltaT] = calculateGalvoDeltaT(vr, beamNum, color, trange, bins, gdtrange)
            %function [vr,galvoDeltaT] = calculateGalvoDeltaT (vr, beamNum, color, trange, bins, gdtrange)
            % finds the galvoDeltaT that maximizes the square of the
            % histogram of photon counts for the central region of the scan (in given time
            % range)
            %
            % beamNum - which beam to use for calibration (applied to both
            % beams)
            % color - 'r', 'g', 'a' - which color to use for calibration
            % trange - time range to use for calibration. best to pick a
            % time when larva was immobilized
            % bins (optional argument) - x;y bin edges to use for histogram
            % generation; if not specified, central 40% of scan is used.
            % gdtrange - range of possible galvoDeltaT values, in
            % microseconds, default [-120 0]
            if (~existsAndDefault('bins', [-50:1:50;-50:1:50]))
                try 
                    is = vr.settings(end).imagingSettings;
                    pixelSize = is.pixelSize_Microns;
                    bins(1,:) = -0.4*is.xScanRange_Microns:pixelSize:0.4*is.xScanRange_Microns;
                    bins(2,:) = -0.4*is.yScanRange_Microns:pixelSize:0.4*is.yScanRange_Microns;
                    
                catch
                    bins = [-50:1:50;-50:1:50];
                end
            end
            existsAndDefault('gdtrange', [-120 0]);
            function negvar = negativeVarianceOfHistogram(gdt)
                pos = vr.getPhotonLocations(beamNum, color, trange, gdt/1e6);
                h = histcounts2(pos(1,:), pos(2,:), bins(1,:), bins(2,:));
                negvar = -var(h(:));
            end
             function neqsqr = negativeSquareOfHistogram(gdt)
                pos = vr.getPhotonLocations(beamNum, color, trange, gdt/1e6);
                h = histcounts2(pos(1,:), pos(2,:), bins(1,:), bins(2,:));
                neqsqr = -sum(h(:).^2);
             end
             gdttest = linspace(min(gdtrange), max(gdtrange),10);
             for j = 1:length(gdttest)
                 v(j) = negativeSquareOfHistogram(gdttest(j));
             end
             [~,I] = min(v);
             gdtrange = gdttest([max(I-1,1), min(I+1,length(gdttest))]);
%             plot (gdttest, v)
          %  options = optimset('Display','iter');
            op = optimset('fminbnd');
            op.TolX = 0.1; %us
            galvoDeltaT = fminbnd(@negativeSquareOfHistogram, min(gdtrange), max(gdtrange), op)/1e6;
            vr.galvoDeltaT = galvoDeltaT;
            [vr.vrfa.galvoDeltaT] = deal(galvoDeltaT);
        end
        
