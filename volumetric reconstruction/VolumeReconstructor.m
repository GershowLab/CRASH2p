            savetodisk = false; % save v_smoo as both .mat and .tif stacks
            dstdir = []; %#ok<NASGU>
            saveopts = struct('overwrite',true); %#ok<NASGU> % see saveastiff for available options
            
            assignApplicable(varargin);
            
            % parse infilling params
            if ~exist('trange','var') || ~exist('tstep','var')
                warning('specify trange and tstep!');
                return;
            end
            
            % parse binning params
            % TODO need to handle scan offset correctly; for now can
            % manually set ranges
            is = vr.settings(end).imagingSettings;
            if ~isfield(ranges,'x')
                ranges.x = [-is.xScanRange_Microns/2 is.xScanRange_Microns/2];
            end
            if ~isfield(ranges,'y')
                ranges.y = [-is.yScanRange_Microns/2 is.yScanRange_Microns/2];
