        function [pos, phase, time, offset] = getPhotonLocations(vr, beamnum, color, timerange, galvoDeltaT, offsetCorrect, phaseCorrect, scanCorrect)
        % [pos, phase, time, offset] = getPhotonLocations(vr, beamnum, color, timerange, galvoDeltaT, offsetCorrect, phaseCorrect, scanCorrect)
        % 
        % gets time and locations from which photons were detected
        % beamnum = 1,2 which beam
        % color = 'r', 'g', 'a' - red channel, green channel, all
        % timerange = time interval in which photons are returned
        % galvoDeltaT (optional) - override vr setting - [] use vr setting
        % offsetCorrect (default false) - if true, pos(1:2,:) = pos(1:2,:) - offset(1:2,:)
        % phaseCorrect (default true) - if true, phase = phase - vr.phaseCorrection(time)
        % scanCorrect (default false) - if true, pos = vr.scanCorrectedPoints(pos,time)
        % pos = x,y position of scan when photons were emitted pos(3) =
        % piezo position
        % phase = phase of tag lens (between 0 & 2pi) when photons were
        % emitted
        % time = time when photons were emitted
        % corrects for phase and beam misalignment, but does not
        % subtract offset or do further registration
            
            existsAndDefault('galvoDeltaT', vr.galvoDeltaT);
            existsAndDefault('offsetCorrect', false);
            existsAndDefault('phaseCorrect', true);
            existsAndDefault('scanCorrect', false);
            if (~existsAndDefault('galvoDeltaT', 0))
                warning ('galvoDeltaT not set - using 0 deltaT');
            end
            
            [vr.vrfa.galvoDeltaT] = deal(galvoDeltaT);
            
            [pos, phase, time] = vr.vrfa.getPhotonLocations(beamnum, color, timerange);
            if (color == 'g')
                pos(1,:) = pos(1,:) - vr.colorMisalignment(1,beamnum);
                pos(2,:) = pos(2,:) - vr.colorMisalignment(2,beamnum);
            end
            if (phaseCorrect)
                phase = mod(phase-vr.getPhaseCorrection(beamnum, time), 2*pi);
            end
            if (nargout > 3 || offsetCorrect)
                [xo,yo,zo] = vr.getOffset(time);
                offset = [xo;yo;zo];
            end
            if (offsetCorrect)
                pos(1:2,:) = pos(1:2,:) - offset(1:2,:);
            end
            if (scanCorrect)
                pos = vr.scanCorrectedPoints(pos,time);
            end
        end
        
