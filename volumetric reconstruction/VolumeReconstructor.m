        function [v_smoo, v, err] = getSmoothedData(vr, trange, tstep, varargin)
            % default params
            scanbeam = 2;
            mode = 'counts'; % 'rate'
            ranges = struct('t',[]);
            voxelsize = [];
            smoothopts = struct([]); % see l1spline for available options
